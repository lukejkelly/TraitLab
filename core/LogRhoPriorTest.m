function tests = LogRhoPriorTest
    tests = functiontests(localfunctions);
end

function valueTest(testCase)
    % compare log PDF to R dgamma at various values of rho
    rho = [...
        1.966725446e-04, 2.067743494e-04, 5.611312703e-05, 1.574548063e-04, ...
        4.314408033e-04, 4.374967962e-04, 1.706383041e-04, 4.382699049e-04, ...
        9.184541392e-05, 2.141345846e-04 ...
    ];
    lObs = arrayfun(@LogRhoPrior, rho);
    lExp = [...
        7.646224091, 7.620759048, 7.721936000, 7.731111951, 6.865177886, ...
        6.841867456, 7.705398293, 6.838884692, 7.789643116, 7.601446181 ...
    ];

    assertEqual(testCase, lObs, lExp, 'AbsTol', 1e-6);
end
